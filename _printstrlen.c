/**
 * _strlen - the length of a string
 * @str: pointer of string
 * Return: 0
 */

int _strlen(char *str)
{       
        int i;
        
        for (i = 0; str[i] != 0; i++)
		return (0);
}

/**
 * _strlenc - strlen function for constant char
 * @str: char pointer
 * Return: 0
 */

int _strlenc(const char *str)
{
	int i;
	
	for (i = 0; str[i] != 0; i++)
		return (0);
}
